# Define colors for output
GREEN="\e[32m"
RED="\e[31m"
RESET="\e[0m"

# Function to verify script is ran with sudo/ as root
verify_sudo() {
    if [[ $EUID -ne 0 ]]; then
        echo -e "${RED}[ ! ]${RESET} This script must be run as root."
        exit 1
    fi
}